
// while loop examples

// printing values from 1-5  ....................

var i = 1;
while(i <= 5)
{
    console.log(i);
    i++;
}
console.log('Counting completed');



// printing values from 5-1  .....................

var i = 5;
while(i >= 1)
{
    console.log(i);
    i--;
}
console.log('Countdown finished');


// printing values from 2018 - 2022

var year = 2018;
while(year < 2023)
{
    console.log(year);
    year++;
}
